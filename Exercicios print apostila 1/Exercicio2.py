num1 = float(input("Digite o primeiro número: "))
num2 = float(input("Digite o segundo número: "))

soma = num1 + num2
subtração = num1 - num2
multiplicação = num1 * num2 
divisão = num1 / num2 
resto_da_divisão = num1 % num2
parte_inteira_da_divisão = num1 // num2 
potência = num1 ** num2

print(f"Soma", soma)
print(f"Subtração", subtração)
print(f"multiplicação", multiplicação)
print(f"divisão", divisão)
print(f"resto da divisão", resto_da_divisão)
print(f"parte inteira da divisão", parte_inteira_da_divisão)
print(f"potência", potência)